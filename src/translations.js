// Language names and flags
export const languages = {
  it: { name: "Italiano", flag: "🇮🇹" },
  en: { name: "English", flag: "🇬🇧" },
  de: { name: "Deutsch", flag: "🇩🇪" },
  es: { name: "Español", flag: "🇪🇸" },
  fr: { name: "Français", flag: "🇫🇷" }
};

// Get translation by key
export function t(key, lang = 'it') {
  const translation = translations[key];
  if (!translation) {
    console.warn(`Translation not found for key: ${key}`);
    return key;
  }
  return translation[lang] || translation['it'] || key;
}

// Get current language from localStorage or browser
export function getCurrentLanguage() {
  const stored = localStorage.getItem('localpoint-lang');
  if (stored && languages[stored]) {
    return stored;
  }

  // Detect browser language
  const browserLang = navigator.language.slice(0, 2).toLowerCase();
  if (languages[browserLang]) {
    return browserLang;
  }

  return 'it'; // Default to Italian
}

// Set language and persist
export function setLanguage(lang) {
  if (!languages[lang]) {
    console.warn(`Language ${lang} not supported`);
    return;
  }

  localStorage.setItem('localpoint-lang', lang);
  document.documentElement.lang = lang;

  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translation = t(key, lang);

    // Check if it's innerHTML (contains HTML tags) or textContent
    if (translation.includes('<')) {
      el.innerHTML = translation;
    } else {
      el.textContent = translation;
    }
  });

  // Update language selector display
  const langDisplay = document.getElementById('current-lang');
  if (langDisplay) {
    langDisplay.textContent = languages[lang].flag;
  }

  // Dispatch event for other scripts
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}
